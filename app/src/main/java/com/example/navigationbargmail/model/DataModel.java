package com.example.navigationbargmail.model;

public class DataModel {
    String id,name;

    public DataModel() {
    }

    public DataModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
